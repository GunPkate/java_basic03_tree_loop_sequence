import java.util.Scanner;

public class sequence5 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		double total = 0;
		for(int i = 1; i<= n; i++) {
			total += 1.0/(i*i);
//			System.out.print(i+" ");
//			System.out.println(total);
		}
		System.out.println(Math.sqrt(6*total));
	}
}
